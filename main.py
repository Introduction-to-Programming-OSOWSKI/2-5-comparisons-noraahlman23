def greaterThan(x,y):
    if x > y:
        return True
    else: 
        return False

print (greaterThan(2,1))

def lessThan(x,y):
    if x < y:
        return True
    else:
        return False
print (lessThan(1,2))

def equalTo(x,y):
    if x == y:
        return True
    else:
        return False

print (equalTo(3,3))

def greaterOrEqual(x,y):
    if x >= y:
        return True 
    else:
        return False

print (greaterOrEqual(4,3))

def lessOrEqual(x,y):
    if x <= y:
        return True
    else:
        return False 

print (lessOrEqual(3,4))